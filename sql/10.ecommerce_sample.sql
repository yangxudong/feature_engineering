INSERT OVERWRITE TABLE ecommerce_sample_v2 PARTITION(dt)
SELECT /*+MAPJOIN(C1,C2)*/
    A.`(dt|pre_day)?+.+`,
    C.`(dt|product_id)?+.+`,
    IF(F.min_price IS NULL OR F.min_price=0, 0, (A.price-F.min_price)/F.min_price) as item_price_diff_ratio,
    C1.`(field_idx|dt|field|event_type)?+.+`,
    C2.`(field_idx|dt|field|event_type)?+.+`,
    A.dt
FROM (
  SELECT *, TO_CHAR(DATEADD(TO_DATE(dt, 'yyyymmdd'), -1, 'dd'), 'yyyymmdd') pre_day
  FROM ecommerce_user_sample_v2
  WHERE dt>'20191031'
) A
LEFT JOIN (
    SELECT *
    FROM ecommerce_item_all_feature WHERE dt >= '20191031'
) C
ON A.pre_day=C.dt AND A.product_id = C.product_id
LEFT JOIN (
    SELECT * FROM ecommerce_item_min_price_di WHERE dt >= '20191031'
) F
ON A.pre_day=F.dt AND A.product_id=F.product_id
LEFT JOIN (
    SELECT *
    FROM pai_rec_dev.ecommerce_item_bin_count
    WHERE dt>='20191031' AND field='cate_hour' AND event_type='view'
) C1
ON A.pre_day=C1.dt AND A.category_idx*5+A.hour_bin = C1.field_idx
LEFT JOIN (
    SELECT *
    FROM pai_rec_dev.ecommerce_item_bin_count
    WHERE dt>='20191031' AND field='cate_hour' AND event_type='cart'
) C2
ON A.pre_day=C2.dt AND A.category_idx*5+A.hour_bin = C2.field_idx
